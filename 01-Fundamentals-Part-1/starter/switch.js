const day = prompt("Enter the day of the week: ");
switch (day) {
    case "monday":
        alert("monday");
        break;
    case "tuesday":
        alert("tuesday");
        break;
    case "wednesday":
        alert("wednesday");
        break;
    case "thursday":
        alert("thursday");
        break;
    case "friday":
        alert("friday");
        break;
    case "saturday":
        alert("saturday");
        break;
    case "sunday":
        alert("sunday");
        break;
    default:
        console.log("Not a valid day");
        break;
}